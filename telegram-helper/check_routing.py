#!/usr/bin/env python3
"""
Детектор дрейфа маршрутизации Помощника.
Запуск: python3 telegram-helper/check_routing.py
Должен завершиться OK — если что-то сломалось после изменений, покажет что именно.
"""
import re, sys

FAIL = []

_NAMES_PAT = (
    r"(виктор\w{0,3}|евгени\w{0,2}|жен\w{0,2}|надежд\w{0,2}|над[еяюьи]"
    r"|управляющ\w{0,4}|технич\w+\s+директор\w{0,2}|техдиректор\w{0,2}|тех\.директор\w{0,2})"
)
_TASK_FOR_RE = re.compile(
    r"задач[ауие]?\s+(?:для\s+)?" + _NAMES_PAT +
    r"[:\s,–-]+(.+?)(?:\s+(?:до|срок|дедлайн)\s+(.+))?$",
    re.IGNORECASE | re.DOTALL,
)
_PROJ_RE = re.compile(
    r"(?:(?:заметка|идея)\s+)?(?:для\s+)?(?:проект(?:а|е|у)?\s+)?"
    r"(губах[аиу]?|юникорн|unicorn|монблан[аe]?|прочее?|офис|разное|без\s+проекта)"
    r"\W+(.+)",
    re.IGNORECASE | re.DOTALL,
)

TASK_CASES = [
    ("Задача для Жени проверить цены", "жени"),
    ("Задача управляющему сделать обход", "управляющему"),
    ("Задача техническому директору проверить бойлеры", "техническому директору"),
    ("Задача для Виктора новый отчёт", "виктора"),
    ("Задача Надежде запустить рекламу", "надежде"),
    ("Задача для Евгению обновить тарифы", "евгению"),
    ("Задача для управляющего проверить номера", "управляющего"),
    ("Задача тех.директора починить насос", "тех.директора"),
]
PROJ_CASES = [
    "Идея для Губахи. Новый сервис",
    "Заметка для проекта Монблан: обновить меню",
    "Идея для Юникорн — запустить подписку",
    # "Заметка: текст" без проекта → SQLite (личное), НЕ проверяем здесь
]

for text, expected in TASK_CASES:
    m = _TASK_FOR_RE.search(text)
    if not m:
        FAIL.append(f"TASK no match: «{text}»")
    elif m.group(1).lower() != expected:
        FAIL.append(f"TASK wrong name: «{text}» → {m.group(1)!r}, ожидалось {expected!r}")

for text in PROJ_CASES:
    if not _PROJ_RE.match(text.strip()):
        FAIL.append(f"PROJ no match: «{text}»")

if FAIL:
    print("FAILED:")
    for f in FAIL:
        print(" ", f)
    sys.exit(1)

print(f"OK — {len(TASK_CASES)} task + {len(PROJ_CASES)} proj детекторов прошли")
